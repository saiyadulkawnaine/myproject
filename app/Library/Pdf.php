<?php
require_once __DIR__.'/tcpdf/tcpdf.php';
 
class Pdf extends TCPDF
{
	private $header;
	  //Page header
    public function Header() {
        // Logo
        $image_file ='images/logo/logo_21.png';
        $image_company ='images/logo/'.$this->header['logo'];
        $this->Image($image_file, 10, 5, 30, '', 'PNG', '', 'T', false, 300, '', false, false, 0, false, false, false);
        $this->Image($image_company, 70, 2, 70, '', 'PNG', '', 'T', false, 300, 'C', false, false, 0, false, false, false);
        //$this->Image($image_file, 160, 5, 30, '', 'PNG', '', 'T', false, 300, '', false, false, 0, false, false, false);
        // Set font
        $this->SetFont('helvetica', 'R', 8);
        $this->SetY(32);
        
        $this->Cell(0, 40, $this->header['address'], 0, false, 'C', 0, '', 0, false, 'M', 'M');
        $this->SetFont('helvetica', 'B', 10);
        //$this->SetFont('helvetica', 'B', 20);
        if(isset($this->header['barcodeno']) && isset($this->header['barcodestyle']))
        {
            $this->SetY(3);
            $this->SetX(190);
            $this->write1DBarcode(str_pad($this->header['barcodeno'],10,0,STR_PAD_LEFT), 'C39', '', '', '', 18, 0.4, $this->header['barcodestyle'], 'N');  
        }
        
        $this->SetY(36);
        $this->SetFont('helvetica', 'B', 10);
        $title="<u>".$this->header['title']."</u>";
        $this->WriteHtml($title, true, false,true,false,'C');
        //$this->Cell(0, 30, $this->header['title'], 0, false, 'C', 0, '', 0, false, 'M', 'M');
        $this->SetY(40);
        //$this->Line(0, 0, 0, 0, '');
    }

    // Page footer
    public function Footer() {
        // Position at 15 mm from bottom
        $this->SetY(-14);
        $this->Cell(0, 10, 'Generated By FamKam ERP ', 0, false, 'C', 0, '', 0, false, 'T', 'M');
        $this->SetY(-10);
        // Set font
        $this->SetFont('helvetica', 'I', 8);
        // Page number
        $this->Cell(0, 10, 'Page '.$this->getAliasNumPage().'/'.$this->getAliasNbPages()."          Print Date: ".date('d-m-Y h:i:s'), 0, false, 'C', 0, '', 0, false, 'T', 'M');
    }

    public function setCustomHeader($header)
    {
       $this->header=$header;
    }
	
}
